/*
  Это JavaScript-файл — он отвечает за ПОВЕДЕНИЕ сайта,
  то есть за то, что происходит в ответ на действия человека.

  Сейчас тут одна задача: когда гость заполнил форму и нажал
  "Отправить заявку" — показать ему сообщение "спасибо",
  а не перезагружать страницу.
*/

// 1) Находим на странице нашу форму по её id="bookingForm".
//    document — это вся страница. querySelector — "найди элемент".
const form = document.querySelector('#bookingForm');

// 2) Находим пустой абзац, куда впишем сообщение об успехе.
const successMessage = document.querySelector('#formSuccess');

// 3) "Слушаем" событие 'submit' — момент, когда форму отправляют.
//    Когда это случится, выполнится функция внутри.
form.addEventListener('submit', function (event) {
  // По умолчанию браузер при отправке формы перезагружает страницу.
  // preventDefault() говорит: "не делай этого, я сам всё обработаю".
  event.preventDefault();

  // 4) Считываем, что человек ввёл в поле "имя".
  //    form.name.value — значение поля с name="name".
  const name = form.name.value;

  // 5) Показываем благодарность с именем гостя.
  successMessage.textContent =
    'Спасибо, ' + name + '! Заявка принята — мы перезвоним в ближайшее время. ☕';

  // 6) Очищаем форму, чтобы поля снова стали пустыми.
  form.reset();
});
